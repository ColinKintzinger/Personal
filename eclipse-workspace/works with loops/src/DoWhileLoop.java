
public class DoWhileLoop {

	public static void main(String[] args) {
		// print 1 to 5 numbers vertically using do while loop
		int c = 1;
		do {
			System.out.println(c);
			c+=1;
		} while(c <= 5);
		// print 2-10 evens
		
		c=2;
		do {
			System.out.println(c);
			c+=2;
		} while(c <= 10);
	}

}
